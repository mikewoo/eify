    creator_id  BIGINT           NOT NULL DEFAULT 0
);
CREATE INDEX IF NOT EXISTS idx_ai_workflow_node_workflow ON ai_workflow_node(workflow_id);

-- 工作流连线表
CREATE TABLE IF NOT EXISTS ai_workflow_edge (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workflow_id    BIGINT      NOT NULL,
    source_node_id BIGINT      NOT NULL,
    target_node_id BIGINT      NOT NULL,
    source_handle  VARCHAR(50) NOT NULL DEFAULT 'default',
    label          VARCHAR(50),
    created_at     TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at     TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    deleted        SMALLINT    NOT NULL DEFAULT 0,
    creator_id     BIGINT      NOT NULL DEFAULT 0
);
CREATE INDEX IF NOT EXISTS idx_ai_workflow_edge_workflow ON ai_workflow_edge(workflow_id);
CREATE INDEX IF NOT EXISTS idx_ai_workflow_edge_source ON ai_workflow_edge(source_node_id);

-- 工作流执行记录表
CREATE TABLE IF NOT EXISTS ai_workflow_execution (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workflow_id      BIGINT      NOT NULL,
    workflow_version INT,
    status           VARCHAR(20) NOT NULL DEFAULT 'running',
    variables        JSONB,
    current_node_id  BIGINT,
    error_message    TEXT,
    started_at       TIMESTAMPTZ,
    completed_at     TIMESTAMPTZ,
    created_at       TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at       TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    deleted          SMALLINT    NOT NULL DEFAULT 0,
    creator_id       BIGINT      NOT NULL DEFAULT 0
);
CREATE INDEX IF NOT EXISTS idx_ai_workflow_execution_workflow ON ai_workflow_execution(workflow_id);
CREATE INDEX IF NOT EXISTS idx_ai_workflow_execution_status ON ai_workflow_execution(status);

-- ############################################################
-- 8. 向量模块（pgvector）
-- ############################################################

-- 文档分块向量表（embedding 为变长向量，维度由对应知识库决定）
CREATE TABLE IF NOT EXISTS document_chunk (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workspace_id BIGINT      NOT NULL DEFAULT 0,
    knowledge_id BIGINT      NOT NULL,
    document_id  BIGINT      NOT NULL,
    chunk_index  INT         NOT NULL,
    content      TEXT        NOT NULL,
    embedding    VECTOR,
    chunk_hash   CHAR(64)    NOT NULL,
    enabled      SMALLINT    NOT NULL DEFAULT 1,
    created_at   TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS idx_chunk_embedding ON document_chunk USING hnsw (embedding vector_cosine_ops);
CREATE INDEX IF NOT EXISTS idx_chunk_workspace ON document_chunk(workspace_id);
CREATE INDEX IF NOT EXISTS idx_chunk_knowledge ON document_chunk(knowledge_id);
CREATE INDEX IF NOT EXISTS idx_chunk_document ON document_chunk(document_id);
CREATE INDEX IF NOT EXISTS idx_chunk_hash ON document_chunk(chunk_hash);

