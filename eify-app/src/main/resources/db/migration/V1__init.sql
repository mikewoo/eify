-- ============================================================
-- Eify 数据库初始化脚本（PostgreSQL 17，最终态）
-- 由 Flyway 自动执行
-- 由 MySQL V1 + V4~V7 增量折叠而成，并合并 document_chunk 向量表
-- ============================================================
--
-- 表清单（按模块分组）：
--   用户与工作空间：ai_user, ai_workspace, ai_workspace_member, ai_workspace_invite, ai_user_session
--   Provider：       provider, model_config, provider_health
--   Agent：          ai_agent
--   Chat：           ai_chat_session, ai_chat_message
--   Knowledge：      knowledge_base, document, agent_knowledge
--   MCP：            mcp_server, mcp_tool, agent_mcp_tool
--   Workflow：       ai_workflow, ai_workflow_node, ai_workflow_edge, ai_workflow_execution
--   向量：           document_chunk
--
-- 规范来源：docs/guides/DATABASE.md, docs/guides/AUTH-WORKSPACE.md
-- ============================================================

CREATE EXTENSION IF NOT EXISTS vector;

-- ############################################################
-- 1. 用户与工作空间模块
-- ############################################################

-- 用户表
CREATE TABLE IF NOT EXISTS ai_user (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username      VARCHAR(64)   NOT NULL,
    email         VARCHAR(128)  NOT NULL DEFAULT '',
    password      VARCHAR(256)  NOT NULL,
    display_name  VARCHAR(128),
    avatar_url    VARCHAR(512),
    status        SMALLINT      NOT NULL DEFAULT 1,
    last_login_at TIMESTAMPTZ,
    created_at    TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    updated_at    TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    deleted       SMALLINT      NOT NULL DEFAULT 0,
    creator_id    BIGINT,
    CONSTRAINT uk_username UNIQUE (username),
    CONSTRAINT uk_email UNIQUE (email)
);

-- 工作空间表
CREATE TABLE IF NOT EXISTS ai_workspace (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name        VARCHAR(128) NOT NULL,
    description VARCHAR(512),
    created_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    deleted     SMALLINT     NOT NULL DEFAULT 0,
    creator_id  BIGINT       NOT NULL DEFAULT 0
);

-- 工作空间成员表
CREATE TABLE IF NOT EXISTS ai_workspace_member (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workspace_id BIGINT      NOT NULL,
    user_id      BIGINT      NOT NULL,
    role         VARCHAR(32) NOT NULL DEFAULT 'member',
    joined_at    TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    created_at   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    deleted      SMALLINT    NOT NULL DEFAULT 0,
    creator_id   BIGINT      NOT NULL DEFAULT 0,
    CONSTRAINT uk_workspace_user UNIQUE (workspace_id, user_id)
);
CREATE INDEX IF NOT EXISTS idx_ai_workspace_member_user_id ON ai_workspace_member(user_id);

-- 工作空间邀请码表
CREATE TABLE IF NOT EXISTS ai_workspace_invite (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workspace_id BIGINT      NOT NULL,
    code         VARCHAR(16) NOT NULL,
    expires_at   TIMESTAMPTZ,
    max_uses     INT         NOT NULL DEFAULT 0,
    use_count    INT         NOT NULL DEFAULT 0,
    enabled      SMALLINT    NOT NULL DEFAULT 1,
    created_at   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    deleted      SMALLINT    NOT NULL DEFAULT 0,
    creator_id   BIGINT      NOT NULL DEFAULT 0,
    CONSTRAINT uk_code UNIQUE (code)
);
CREATE INDEX IF NOT EXISTS idx_ai_workspace_invite_workspace_id ON ai_workspace_invite(workspace_id);

-- 用户会话表（refresh token 持久化）
CREATE TABLE IF NOT EXISTS ai_user_session (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id       BIGINT       NOT NULL,
    refresh_token VARCHAR(256) NOT NULL,
    expires_at    TIMESTAMPTZ  NOT NULL,
    created_at    TIMESTAMPTZ  NOT NULL DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS idx_user_session_user_id ON ai_user_session(user_id);
CREATE INDEX IF NOT EXISTS idx_refresh_token ON ai_user_session(refresh_token);

-- ############################################################
-- 2. Provider 模块
-- ############################################################

-- 模型供应商表
CREATE TABLE IF NOT EXISTS provider (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workspace_id BIGINT       NOT NULL DEFAULT 1,
    name         VARCHAR(100) NOT NULL,
    type         VARCHAR(50)  NOT NULL,
    base_url     VARCHAR(500) NOT NULL,
    auth_config  JSONB        NOT NULL,
    enabled      SMALLINT     NOT NULL DEFAULT 1,
    created_at   TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    updated_at   TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    deleted      SMALLINT     NOT NULL DEFAULT 0,
    creator_id   BIGINT       NOT NULL DEFAULT 0,
    CONSTRAINT uk_provider_name_workspace_deleted UNIQUE (name, workspace_id, deleted)
);
CREATE INDEX IF NOT EXISTS idx_provider_type_enabled ON provider(type, enabled);
CREATE INDEX IF NOT EXISTS idx_provider_deleted ON provider(deleted);

-- 模型配置表
CREATE TABLE IF NOT EXISTS model_config (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    provider_id    BIGINT       NOT NULL,
    name           VARCHAR(100) NOT NULL,
    model_id       VARCHAR(100) NOT NULL,
    model_category SMALLINT     NOT NULL DEFAULT 0,
    context_size   INT          NOT NULL DEFAULT 0,
    extra_params   JSONB        NOT NULL,
    enabled        SMALLINT     NOT NULL DEFAULT 1,
    created_at     TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    updated_at     TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    deleted        SMALLINT     NOT NULL DEFAULT 0,
    creator_id     BIGINT       NOT NULL DEFAULT 0,
    workspace_id   BIGINT       NOT NULL DEFAULT 1
);
CREATE INDEX IF NOT EXISTS idx_model_config_provider_id ON model_config(provider_id);
CREATE INDEX IF NOT EXISTS idx_model_config_model_id ON model_config(model_id);
CREATE INDEX IF NOT EXISTS idx_model_config_enabled_deleted ON model_config(enabled, deleted);
CREATE INDEX IF NOT EXISTS idx_model_category ON model_config(model_category);

-- 供应商健康状态表
CREATE TABLE IF NOT EXISTS provider_health (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    provider_id     BIGINT       NOT NULL,
    status          VARCHAR(20)  NOT NULL DEFAULT 'UNKNOWN',
    last_check_at   TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    last_success_at TIMESTAMPTZ,
    fail_count      INT          NOT NULL DEFAULT 0,
    latency_ms      INT,
    error_message   VARCHAR(500),
    created_at      TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    deleted         SMALLINT     NOT NULL DEFAULT 0,
    creator_id      BIGINT       NOT NULL DEFAULT 0,
    CONSTRAINT uk_provider_id UNIQUE (provider_id)
);

-- ############################################################
-- 3. Agent 模块
-- ############################################################

-- Agent 配置表
CREATE TABLE IF NOT EXISTS ai_agent (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workspace_id        BIGINT        NOT NULL DEFAULT 1,
    name                VARCHAR(100)  NOT NULL,
    description         VARCHAR(500),
    avatar              VARCHAR(500),
    default_provider_id BIGINT        NOT NULL,
    default_model       VARCHAR(100)  NOT NULL,
    system_prompt       TEXT          NOT NULL,
    user_message_prefix VARCHAR(1000),
    welcome_message     VARCHAR(500),
    temperature         NUMERIC(3,2)  NOT NULL DEFAULT 0.70,
    max_tokens          INT           NOT NULL DEFAULT 2000,
    top_p               NUMERIC(3,2)  NOT NULL DEFAULT 1.00,
    frequency_penalty   NUMERIC(3,2)  NOT NULL DEFAULT 0.00,
    presence_penalty    NUMERIC(3,2)  NOT NULL DEFAULT 0.00,
    max_history_rounds  INT           NOT NULL DEFAULT 10,
    stream_enabled      SMALLINT      NOT NULL DEFAULT 1,
    workflow_id         BIGINT,
    rag_enabled         SMALLINT      NOT NULL DEFAULT 0,
    rag_top_k           INT           NOT NULL DEFAULT 5,
    rag_strategy        VARCHAR(20)   NOT NULL DEFAULT 'hybrid',
    agent_config        JSONB,
    enabled             SMALLINT      NOT NULL DEFAULT 1,
    created_at          TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    deleted             SMALLINT      NOT NULL DEFAULT 0,
    creator_id          BIGINT,
    CONSTRAINT uk_agent_name_workspace_deleted UNIQUE (name, workspace_id, deleted)
);
CREATE INDEX IF NOT EXISTS idx_ai_agent_default_provider_id ON ai_agent(default_provider_id);
CREATE INDEX IF NOT EXISTS idx_ai_agent_enabled_deleted ON ai_agent(enabled, deleted);
CREATE INDEX IF NOT EXISTS idx_ai_agent_created_at ON ai_agent(created_at);
CREATE INDEX IF NOT EXISTS idx_ai_agent_workspace_id ON ai_agent(workspace_id);

